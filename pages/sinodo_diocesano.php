              <h4>¿QUÉ ES UN SÍNODO DIOCESANO?</h4> 
            </div>
          </div>
          <div class="col-sm-1">
              <img class="img-responsive" src="../images/sinodo.png">
          </div>
          </div>
        </div>
      </div>
      <div class="row cita">
        <div class="col-sm-3"></div>
        <div class="col-sm-6">
          <div class="col-sm-12">
            <div class="col-sm-12">
              <br>
              <p align="center"><b><i>"El sínodo diocesano es una asamblea de sacerdotes y de otros fieles escogidos de una Iglesia particular, que prestan su ayuda al Obispo de la Diócesis para bien de toda la comunidad diocesana"</i> (CIC 460).</b></p>
               <p align="center"><b><i>"El Sínodo diocesano es, por ello, un momento privilegiado de iluminación de la conciencia de ser la Iglesia querida por el Padre, fundada por Cristo  y asistida por el Espíritu Santo; es también un momento singular de discernimiento de la voluntad actual de Dios para esta Iglesia local, escrutada en los acontecimientos que constituyen los signos de los tiempos"</i> (CD 829).</b></p>
               <p align="center"><b><i>"El Sínodo diocesano significa el modo más destacado y solemne que tiene la Iglesia local, en comunión con su Obispo, para vivir su misión de Iglesia"</i> (CD 825).</b></p>
            </div> 
          </div> 
        </div>
      </div>
      <div class="row cont">
        <div class="col-sm-12">
            <div class="col-sm-3"></div>
          <div class="col-sm-6">
            <img class="img-responsive" src="../images/sinodal.jpg">
          </div>
        </div>
        <div class="col-sm-12">
          <div class="col-sm-12">
            <div class="col-sm-12">
              <br>
              <p>La Iglesia convocó desde el principio asambleas, empezando por la de Jerusalén <b>(Hech. 15, 1-30)</b>, para dar respuestas nuevas a nuevas situaciones, y nuevos interrogantes. Y llamó a estas asambleas <b>"sínodos"</b>.</p>
              <p>La palabra sínodo es una palabra que viene de la lengua griega. En esta lengua <b>"camino"</b> se dice <b>"ódos"</b> y nuestra preposición <b>"con"</b> se dice <b>"sin"</b>. De ahí la palabra <b>"sin-odo"</b>. <b>"camino con"</b>, camino en común,caminar con otros, o caminar juntos.</p>
              <p>Los sínodos fueron desde los primeros siglos de la Iglesia uno de los medios por los cuales los obispos <i>"movidos por la comunión de la caridad fraterna y por amor a la misión universal conferida a los apostoles, unieron sus fuerzas y voluntades para procurar el bien común y el de las iglesias particulares"</i>. <b>(Vaticano II, Christus Dominus, CD,36)</b></p>
            </div>
          </div>
        </div>
