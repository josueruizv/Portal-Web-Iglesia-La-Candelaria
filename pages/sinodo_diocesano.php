            <div class="col-sm-12">
              <h4>INSTRUCCIÓN PARA LA PARTICIPACIÓN EN LOS SÍNODOS  DIOCESANOS</h4> 
            </div>
          </div>
          <div class="col-sm-2">
              <img class="img-responsive" src="../images/sinodo.png">
          </div>
          </div>
        </div>
      </div>
      <div class="row cont">
        <div class="col-sm-8">
          <div class="col-sm-1"></div>
          <div class="col-sm-11">
            <div class="col-sm-12">
              <br>
              <p>El aporte principal de los sinodales (laicos, pastores y consagrados) es el de formular su parecer o con voto acerca de los temas propuestos por el Obispo en el Sínodo. Se trata de un voto “consultivo”, dado que el Obispo es libre de acoger o no estas opiniones o pareceres, lo cual no significa ignorar su importancia. No es solo un “asesoramiento externo” el que dan los sinodales, porque ellos representan una gran responsabilidad en las conclusiones y resultados del Sínodo.</p>
            </div>
          </div>
        </div>
        <div class="col-sm-3">
          <div class="col-sm-12">
            <img class="img-responsive picture" src="../images/consulta.jpg">
          </div>
        </div>
      </div>
       <div class="row cont">
        <div class="col-sm-12">
          <div class="col-sm-12">
            <div class="col-sm-12">
              <p>Los sinodales, en efecto, son elegidos por su experiencia en la Iglesia y por los consejos que pueden entregar y es por ello que colaboran activamente en la elaboración y redacción de los documentos sinodales <b>(Declaraciones y Decretos) (Inst., Cap I, Nº 2).</b></p>

              <p>El Obispo también puede incorporar al Sínodo como observadores <i>"a algunos ministros o miembros de Iglesias, o de comunidades eclesiales que no estén en comunión plena con la Iglesia Católica"</i><b> (Inst., Cap II, Nº 6).</b></p>

              <p>Es de suponer que todos los miembros sinodales están y se sienten en plena comunión con la Iglesia y buscan el bien de todos los fieles de la Diócesis, por lo que el Obispo tiene derecho y deber de remover, a aquellos miembros sinodales que se aparten de la sana doctrina o que rechacen la autoridad episcopal<b> (Inst., Cap II, Nº 5).</b></p> 

              <p>La finalidad principal del Sínodo Diocesano es prestar la ayuda al Obispo en el ejercicio de su función conductora del pueblo de Dios, para el bien de toda la comunidad diocesana. Por lo tanto, el Obispo y los convocados como sinodales ejercen una doble responsabilidad:</p>
              <br>
              <div class="col-sm-1"></div>
              <ul class="col-sm-11">
                <li><p>El Obispo los llama a colaborar con él en su oficio de gobernar a la Iglesia que le ha sido encomendada.</p>
                <li><p>Los sinodales prestan su ayuda al Obispo como cooperadores de la edificación del Pueblo de Dios presente en la Diócesis.</p>
              </ul>
              <p>Lo que define el bien de toda la comunidad diocesana (la finalidad esencial del Sínodo) son los valores de la Comunión y de la Misión, aspectos inseparables de esa finalidad. (Inst., Cap I, Nºs 1,2 y 3).</p>
            </div>
